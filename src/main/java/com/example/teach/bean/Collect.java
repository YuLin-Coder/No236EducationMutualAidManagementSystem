package com.example.teach.bean;

public class Collect {

    private int collectID;
    private String collectPath;
    private int collectUserID;
    private String collectName;
    private String collectPrice;
    private int collectParam;

    public int getCollectID() {
        return collectID;
    }

    public void setCollectID(int collectID) {
        this.collectID = collectID;
    }

    public String getCollectPath() {
        return collectPath;
    }

    public void setCollectPath(String collectPath) {
        this.collectPath = collectPath;
    }

    public int getCollectUserID() {
        return collectUserID;
    }

    public void setCollectUserID(int collectUserID) {
        this.collectUserID = collectUserID;
    }

    public String getCollectName() {
        return collectName;
    }

    public void setCollectName(String collectName) {
        this.collectName = collectName;
    }

    public String getCollectPrice() {
        return collectPrice;
    }

    public void setCollectPrice(String collectPrice) {
        this.collectPrice = collectPrice;
    }

    public int getCollectParam() {
        return collectParam;
    }

    public void setCollectParam(int collectParam) {
        this.collectParam = collectParam;
    }
}
