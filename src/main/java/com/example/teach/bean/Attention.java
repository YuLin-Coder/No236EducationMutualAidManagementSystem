package com.example.teach.bean;

/**
 * @author Monster
 */
public class Attention {
    private int attentionID;
    private String attentionName;
    private String attentionWhoName;

    public int getAttentionID() {
        return attentionID;
    }

    public void setAttentionID(int attentionID) {
        this.attentionID = attentionID;
    }

    public String getAttentionName() {
        return attentionName;
    }

    public void setAttentionName(String attentionName) {
        this.attentionName = attentionName;
    }

    public String getAttentionWhoName() {
        return attentionWhoName;
    }

    public void setAttentionWhoName(String attentionWhoName) {
        this.attentionWhoName = attentionWhoName;
    }
}
