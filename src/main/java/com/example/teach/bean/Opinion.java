package com.example.teach.bean;

public class Opinion {

    private int opinionID;
    private String opinionName;
    private String opinionEmail;
    private String opinionTime;
    private String opinionMessage;
    private String opinionPhone;
    private String sendName;

    public int getOpinionID() {
        return opinionID;
    }

    public void setOpinionID(int opinionID) {
        this.opinionID = opinionID;
    }

    public String getOpinionName() {
        return opinionName;
    }

    public void setOpinionName(String opinionName) {
        this.opinionName = opinionName;
    }

    public String getOpinionEmail() {
        return opinionEmail;
    }

    public void setOpinionEmail(String opinionEmail) {
        this.opinionEmail = opinionEmail;
    }

    public String getOpinionTime() {
        return opinionTime;
    }

    public void setOpinionTime(String opinionTime) {
        this.opinionTime = opinionTime;
    }

    public String getOpinionMessage() {
        return opinionMessage;
    }

    public void setOpinionMessage(String opinionMessage) {
        this.opinionMessage = opinionMessage;
    }

    public String getOpinionPhone() {
        return opinionPhone;
    }

    public void setOpinionPhone(String opinionPhone) {
        this.opinionPhone = opinionPhone;
    }

    public String getSendName() {
        return sendName;
    }

    public void setSendName(String sendName) {
        this.sendName = sendName;
    }
}
